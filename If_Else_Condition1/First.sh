#!/bin/bash -x

read -p "Enter Single digit Number :" input

if [ $input == 0 ]
then
	echo "Zero"
elif [ $input == 1 ]
then
	echo "One"

elif [ $input == 2 ]
then
	echo "Two"

elif [ $input == 3 ]
then
	echo "Three"

elif [ $input == 4 ]
then
	echo "Foue"

elif [ $input == 5 ]
then
	echo "Five"

elif [ $input == 6 ]
then
	echo "Six"

elif [ $input == 7 ]
then
	echo "Seven"

elif [ $input == 8 ]
then
	echo "Eight"

elif [ $input == 9 ]
then
	echo "Nine"
fi
